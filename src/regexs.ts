export const _regex = {
	name: /^[a-zA-Z\.\- ]{3,32}$/,
	username: /^(?=.{3,16}$)(?![_.])(?!.*[_.]{2})[a-z0-9._]+(?<![_.])$/,
	email: /^(([^<>()\[\]\.,;:\s@\"]+(\.[^<>()\[\]\.,;:\s@\"]+)*)|(\".+\"))@(([^<>()[\]\.,;:\s@\"]+\.)+[^<>()[\]\.,;:\s@\"]{2,})$/,
	phone: /^(\+?[0-9\-]{10,15})$/,
	password: /^.{8,20}$/,
	character: /^[a-zA-Z]+$/
}